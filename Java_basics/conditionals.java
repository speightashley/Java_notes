// Conditionals in Java are used to execute code based on a condition.
// The following list of conditionals are used in Java:
// if
// else
// switch

/** conditionals */
public class conditionals {
  public static void main(String[] args) {

    int x = 5;

    if (x < 29) {

      System.out.println("x is less than 29");
    }
    if (x > 29) {
      System.out.println("x is greater than 29");
    }
    if (x == 29) {
      System.out.println("x is equal to 29");
      String name = "Ashley";
      System.out.println(name);
    }
  }
}
